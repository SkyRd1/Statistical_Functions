#day: day.
##############################################################################################
#year: year.
##############################################################################################
#This function returns the julian day corresponding to the input date.
##############################################################################################
def Convert(month,day,year):
#datetime is for date and time processing
    import datetime
#--------------------------------------------------------------------------------------------#
#--------------------------------------------------------------------------------------------#


#--------------------------------------------------------------------------------------------#
#Convert calendar date to julian day
#--------------------------------------------------------------------------------------------#
#put the date in appropriate format
    date = str(year)+'.'+str(month)+'.'+str(day)
    fmt = '%Y.%m.%d'
    dt = datetime.datetime.strptime(date, fmt)
    tt = dt.timetuple()
    return tt.tm_yday
