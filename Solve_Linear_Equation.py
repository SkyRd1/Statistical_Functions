##############################################################################################
#Coef: Aa array of coefficients for each unknown
#the order of unknowns should be same in each equation.
##############################################################################################
#Values: Array of result values for each equation.
##############################################################################################
#This functions returnes values of unknowns.
#(AX = B as a reult X = inverse(A)B)
##############################################################################################
def Solve_Linear_Equation(Coef,Values):
#numpy is for data manipulationt
 import  numpy as np
#--------------------------------------------------------------------------------------------#
#--------------------------------------------------------------------------------------------#


#--------------------------------------------------------------------------------------------#
#Calculating matrices
#--------------------------------------------------------------------------------------------#
 Coef = np.asanyarray(Coef)
 Values = np.asanyarray(Values)
 Inv_Coef = np.linalg.inv(Coef)
#--------------------------------------------------------------------------------------------#
#--------------------------------------------------------------------------------------------#


#--------------------------------------------------------------------------------------------#
#Retrieving unknowns
#--------------------------------------------------------------------------------------------#
 Ans = np.dot(Inv_Coef, Values)
 return Ans
